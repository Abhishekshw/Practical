package switch_break;

public class switchvowel {
	public static void main(String[] args) {
		char ch='e';
		switch(ch) { 
		case 'y':System.out.println("not a vowel");
		break;
		case 'e':System.out.println(" vowel");
		break;
		case 'o':System.out.println("its vowel");
		break;
		default: System.out.println("consontant");
	}

}
}
